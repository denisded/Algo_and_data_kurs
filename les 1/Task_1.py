"""
Выполнить логические побитовые операции «И», «ИЛИ» и др. над числами 5 и 6.
Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.
"""
print(f"Побитовое И на числами 5 и 6 = {5 & 6}")
print(f"Побитовое ИЛИ на числами 5 и 6 = {5 | 6}")
print(f"Побитовый сдвиг вправо на 2 знака над числом 5 = {5 >> 2}")
print(f"Побитовый сдвиг влево на 2 знака над числом 5 = {5 << 2}")
